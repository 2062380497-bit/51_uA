// STC8G1K08 五引脚推挽输出 2秒同步翻转（最终版，严格按文档）
// 翻转引脚：P1.0 / P1.1 / P1.6 / P3.3 / P5.5
// 系统时钟：11.0592MHz
#include "STC8G.H"

#define P55_MASK 0x20


// ========== 定时器0初始化 (设置125us中断) ==========
void Timer0_Init(void)
{
    AUXR |= 0x80;       // 定时器时钟1T模式 (STC特有)
    TMOD &= 0xF0;       // 设置定时器为模式0(16位自动重装载)
    TL0 = 0x9A;         // 设置定时初始值 (65536 - 1382 = 0xFA9A, 对应125us @11.0592MHz)
    TH0 = 0xFA;         // 设置定时初始值
    TF0 = 0;            // 清除TF0标志
    TR0 = 1;            // 定时器0开始计时
    ET0 = 1;            // 使能定时器0中断
    EA = 1;             // 打开总中断
}

// ========== 软件延时函数 (约2秒 @ 11.0592MHz) ==========
void Delay2s(void)
{
    unsigned int i, j;
    for(i = 0; i < 20000; i++)
        for(j = 0; j < 110; j++);
}

// ========== PCA PWM 配置 (P3.5 输出准 800Hz ) ==========
void PWM_Init(void)
{
    // 1. 定时器0 作为 PCA 的时钟源 (提供频率基准)
    // 需要 PCA_Clk = 800Hz * 256 = 204800Hz
    // 系统时钟 11.0592MHz / 204800 = 54 周期
    AUXR |= 0x80;       // 定时器0为1T模式
    TMOD &= 0xF0;       // 定时器0为模式0 (16位自动重装载)
    TL0 = 0xCA;         // 65536 - 54 = 0xFFCA
    TH0 = 0xFF;
    TR0 = 1;            // 启动定时器0

    // 2. 将 PCA 模块 0 的引脚切换到 P3.5
    // P_SW1 寄存器的 CCP_S[1:0] = 01 (即 PCA0 映射到 P3.5)
    P_SW1 = (P_SW1 & ~0x30) | 0x10;

    // 3. 配置 PCA 输出 8位硬件 PWM
    CCON = 0;           // 清除 PCA 控制字
    CMOD = 0x04;        // PCA 时钟选择 = 定时器0溢出脉冲
    CL = 0; 
    CH = 0;
    
    CCAPM0 = 0x42;      // 开启 PCA 模块 0 的 8 位 PWM 模式 (ECOM0=1, PWM0=1)
    PCA_PWM0 = 0x00;    // 8位 PWM，占空比寄存器高位设为 0
    CCAP0H = 128;       // 50% 占空比 (256/2 = 128)
    CCAP0L = 128;
    
    CR = 1;             // 启动 PCA 计时器，自动输出硬件 PWM
}

void main(void)
{
    // ========== P1.0/P1.1/P1.6：推挽输出 ==========
    P1M0 |= 0x01 | 0x02 | 0x40;
    P1M1 &= ~(0x01 | 0x02 | 0x40);
    P10 = 0;
    P11 = 0;
    P16 = 0;

    // ========== P3.5：推挽输出 (作为蜂鸣器) ==========
    P3M0 |= 0x20; 
    P3M1 &= ~0x20;

    // 初始化硬件 PWM！
    PWM_Init();

    // ========== 主循环：现在只需用2秒慢闪LED ==========
    while(1)
    {
        P1 ^= 0x43;   // 翻转 P1.0 | P1.1 | P1.6 (每2秒翻转一次，用于LED)
        Delay2s();
    }
}